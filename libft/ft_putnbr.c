#include "libft.h"

void	ft_putnbr(int n)
{
	char i;

	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			n = -n;
			ft_putchar('-');
		}
		i = n % 10 + '0';
		if (n > 9)
			ft_putnbr(n / 10);
		ft_putchar(i);
	}
}
