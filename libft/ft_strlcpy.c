#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	src_size;
	size_t	i;

	if (!src && !dst)
		return (0);
	src_size = ft_strlen(src);
	i = 0;
	if (dstsize <= 0)
		return (src_size);
	while (src[i] && i < dstsize - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (src_size);
}
